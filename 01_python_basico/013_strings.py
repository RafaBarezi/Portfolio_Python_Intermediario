# strings   str

# upper - Transforma todas as letras em maiúsculas.
# lower - Transforma todas as letras em minúsculas.
# split - Quebra a string em substrings.

curso = "\nEnsino a Distância"
print(curso)
curso = curso.upper()
print(curso)
curso = curso.lower()
print(curso, "\n")
curso = curso.split()
print(curso)